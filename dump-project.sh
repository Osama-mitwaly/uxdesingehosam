#!/bin/bash
# =================================================================
# سكربت تجميع مشروع العرض التفاعلي في ملف واحد للتحليل
# ينتج ملف: project-dump.txt يحتوي على كل الأكواد منظمة
# =================================================================

OUTPUT_FILE="project-dump.txt"
PROJECT_NAME=$(basename "$(pwd)")

echo "📦 جاري تجميع مشروع: $PROJECT_NAME"
echo "📄 الملف الناتج: $OUTPUT_FILE"
echo ""

# بدء الملف برأس تعريفية
cat << EOF > "$OUTPUT_FILE"
================================================================================
📁 مشروع: $PROJECT_NAME
🕐 تاريخ التجميع: $(date '+%Y-%m-%d %H:%M:%S')
🎯 الغرض: تحليل الأعطال واكتشاف المشاكل
================================================================================

📋 فهرس الملفات الموجودة:
$(find . -maxdepth 1 -type f \( -name "*.html" -o -name "*.css" -o -name "*.js" \) ! -name "$OUTPUT_FILE" ! -name "*.sh" -exec basename {} \; | sort | sed 's/^/  • /')

================================================================================
EOF

# دالة لإضافة محتوى ملف مع فاصل واضح
add_file_content() {
    local filepath="$1"
    local filename=$(basename "$filepath")
    
    if [ -f "$filepath" ] && [ -s "$filepath" ]; then
        cat << EOF >> "$OUTPUT_FILE"

📄 الملف: $filename
📍 المسار: $filepath
📏 الحجم: $(wc -c < "$filepath") بايت | $(wc -l < "$filepath") سطر
--------------------------------------------------------------------------------

$(cat "$filepath")

--------------------------------------------------------------------------------
EOF
        echo "✓ تمت إضافة: $filename"
    elif [ -f "$filepath" ]; then
        cat << EOF >> "$OUTPUT_FILE"

📄 الملف: $filename
⚠️  الحالة: ملف فارغ (0 بايت)
--------------------------------------------------------------------------------

[محتوى فارغ]

--------------------------------------------------------------------------------
EOF
        echo "⚠️  ملف فارغ: $filename"
    fi
}

# جمع ملفات المشروع الرئيسية (بدون تكرار أو ملفات غير مرغوبة)
echo "" >> "$OUTPUT_FILE"
echo "🗂️ محتوى الملفات:" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

# HTML Files
for file in index.html merchant-dashboard.html merchant-store-mgmt.html merchant-theme-mgmt.html add-product.html inventory.html; do
    add_file_content "$file"
done

# CSS Files
for file in style.css reset.css variables.css layout.css components.css login.css dashboard.css; do
    add_file_content "$file"
done

# JS Files
for file in app.js login.js navigation.js; do
    add_file_content "$file"
done

# إضافة ملاحظات ختامية
cat << EOF >> "$OUTPUT_FILE"

================================================================================
🔍 ملاحظات للمحلل (الذكاء الاصطناعي):
================================================================================
1. الصفحات الأصلية (الدخول، لوحة التاجر) كانت تعمل ثم انهارت بعد إضافة ميزات المخزون.
2. جدول المخزون لا يظهر عمود "الإجراءات" على شاشات الموبايل.
3. في صفحة add-product: النقر على أي حقل نصي يفتح نافذة اختيار الصورة.
4. أزرار "حفظ" في المودالات شكلها غير متناسق.
5. بعض ملفات الـ CSS كانت فارغة (0 بايت) وقد تكون سبب التعارض.

🎯 المطلوب:
- إصلاح الصفحات الأصلية دون كسر ميزات المخزون الجديدة.
- جعل الجدول متجاوباً بشكل صحيح (عمودين على الموبايل + قائمة إجراءات).
- عزل منطق كل صفحة لمنع التعارضات المستقبلية.

================================================================================
✅ نهاية التقرير - جاهز للتحليل
================================================================================
EOF

echo ""
echo "✅ اكتمل التجميع بنجاح!"
echo "📄 الملف الناتج: $(pwd)/$OUTPUT_FILE"
echo "📏 حجم الملف: $(du -h "$OUTPUT_FILE" | cut -f1)"
echo ""
echo "📋 الخطوات التالية:"
echo "   1. افتح الملف: cat $OUTPUT_FILE"
echo "   2. انسخ المحتوى كاملاً"
echo "   3. الصقه في المحادثة مع الذكاء الاصطناعي للتحليل"
echo ""
echo "💡 ملاحظة: الملف يحتوي فقط على أكواد المشروع، لا يشمل صوراً أو بيانات حساسة."
